public class CharacterDataType {
    public static void main(String[] args) {
        char f = 'f';
        char a = 'a';
        char h = 'h';
        char m = 'm';
        char i = 'i';

        System.out.println(f);
        System.out.println(a);
        System.out.println(h);
        System.out.println(m);
        System.out.println(i);
    }
}

public class If {
    public static void main(String[] args) {
        int number = 10;

        if (number > 10) {
            System.out.println("Number is greater than 10");
        } else if (number < 10) {
            System.out.println("Number is less than 10");
        } else {
            System.out.println("Number is equal to 10");
        }
    }
}
